# Para poder reproduzir um arquivo MP3 no python, siga os passos a seguir:
# 1 - PRIMEIRO É PRECISO TER O ARQUIVO MP3 EM SEU COMPUTADOR, EM SEGUIDA COPIE E COLE O ARQUIVO EM 'PROJECT'.
# 2 - INSTALE O MÓDULO PYGAME
# 3 - IMPORTE O MODULO : import pygame
# 4 - pygame.init()
# 5 - pygame.mixer.music.load('nome do arquivo')
# 6 - pygame.mixer.music.play()
# 7 - pygame.event.wait()
